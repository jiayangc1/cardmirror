#!/usr/bin/env node
/**
 * Bake the relay bearer token into the build from the PAIRING_TOKEN env var.
 *
 * A double-clicked packaged app does NOT inherit shell env, so the token can't
 * be read at runtime — it has to be compiled in. The release CI runs this
 * (with the PAIRING_TOKEN Actions secret) before building the desktop app, so
 * the installers can authenticate to the relay. The card payload is end-to-end
 * encrypted regardless, so this token is only light gating.
 *
 * The real token is NEVER committed: this overwrites the (empty) committed
 * src/pairing-build.ts in CI's ephemeral checkout only. With PAIRING_TOKEN
 * unset it writes the empty form, reproducing the committed file byte-for-byte.
 */
const fs = require('node:fs');
const path = require('node:path');

const token = process.env.PAIRING_TOKEN || '';
const out = path.join(__dirname, '..', 'src', 'pairing-build.ts');
const body =
  '// AUTO-GENERATED by scripts/inject-relay-token.cjs — do not edit.\n' +
  `export const BUILT_IN_RELAY_TOKEN = ${JSON.stringify(token)};\n`;
fs.writeFileSync(out, body);
console.log(`[inject-relay-token] wrote ${out} (token ${token ? 'set' : 'empty'})`);
